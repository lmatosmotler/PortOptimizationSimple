	#problema de alocação de ativos cenário com IBOV e USDBRL, para Meirelles, Alckmin e Manuela D'ávila, nesssa ordem
	# M: 200% -45% A:100% -20% D:-20% 75%
	using GLPKMathProgInterface
	using JuMP
	using LinearAlgebra
	
	A=[0 1 1 0 0 0;-1 3 0.55 -1 0 0;-1 2 0.8 0 -1 0;-1 0.8 1.75 0 0 -1]
	x=[1;1;1;1.0;1;1]
	s=[1;1;1;1.0;1;1]
	b=[1;0.0;0;0]
	c=-[1;0;0;0.0;0;0]
	p=[0;0.0;0;0]
	maxiter=30
	alfa=0.9
	epsilon=0.0001
	ω = 0.001
	
	function interiorpoints(A, x, s, p, maxiter, alfa, b, c, epsilon, ω)
	
	    n = length(x)
	    m = length(p)
	    l = length(s)
	
	    e = ones(n)
	
	    IndX=1:n
	    IndP=n+1:m+n
	    IndS=m+n+1:l+m+n
	
	    k = 0
	    control = 0
	    while control == 0
	
	        mi=ω*(s'*x)/n
	
	        X=Diagonal(x)
	        S=Diagonal(s)
	
	        F=[A*x-b;A'*p+s-c;X*S*e-mi*e]
	        T=[A zeros(m,m) zeros(m,l); zeros(n,n) A' I; S zeros(l,m) X]
	        d=-T\F
	
	        Dx=d[IndX]
	        Dp=d[IndP]
	        Ds=d[IndS]
	
	        betaX=1
	        betaS=1
	
	        for i in 1:n
	            if x[i]+Dx[i]<0.0
	                betaX=min(betaX,(x[i]/abs(Dx[i])))
	                betaX=alfa*betaX
	            end
	            if s[i]+Ds[i]<0.0
	                betaS=min(betaS,(s[i]/abs(Ds[i])))
	                betaS=alfa*betaS
	            end
	        end
	
	
	        x = x + betaX*Dx
	        p = p + betaS*Dp
	        s = s + betaS*Ds
	        k=k+1
	
	        if x'*s<=epsilon
	            control = 1 #se o retorno for 1 significa que o problema eh viável e limitado e portando tem uma solução ótima
	        elseif k>=maxiter
	            control = 2 #se o retorno for 2 significa que o bateu no max iter
	        end
	        for i in 1:n
	            if abs(x[i])>10^9
	                control = 3 #se o retorno for 3 significa que o problema é ilimitado
	            end
	        end
	        for i in 1:m
	            if abs(p[i])>10^9
	                control = 4 #se o retorno for 3 significa que o problema é inviável
	            end
	        end
	    end
	    return x,s,p,control
	end
	
	
	function le_resposta(x,s,p,resp)
	    if resp == 1
	        println("O Problem é viável e o ótimo foi encontrado")
	        print("vetor X: ")
	        println(x)
	        print("vetor S: ")
	        println(s)
	        print("vetor P: ")
	        println(p)
	        println("Deve-se alocar $(x[2]*100)% no ativo 1 e deve-se alocar $(x[3]*100)% no ativo 2")
	    elseif resp == 2
	        println("Provavelmente há algum erro, pois o problema não é nem inviável nem ilimitado, mas bateu o max iter")
	    elseif resp == 3
	        println("PROBLEMA ILIMITADO")
	    elseif resp == 4
	        println("PROBLEMA INVIÁVEL")
	    end
	
	end
	
	println("============================================ NOVO TESTE ==================================================")
	x, s, p, resp = interiorpoints(A,x,s,p,maxiter,alfa,b,c,epsilon,ω)
	
	
	
	le_resposta(x,s,p,resp)
	
end

